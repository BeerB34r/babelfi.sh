#/usr/bin/env bash

# the basics
MAILBOX='/sgoinfre/babelfish/'
MYMAILBOX=$MAILBOX"`whoami`/"
MAILCHECK="${MAILCHECK:-1}"
mkdir --mode=777 -p $MYMAILBOX

appendMailpath() {
	[ -z "$1" ] && return 1

	if [ -z "$MAILPATH" ]; then
		MAILPATH=$1?$2
	elif echo "$MAILPATH" | grep "$1" >/dev/null 2>/dev/null; then
		return 0
	else
		MAILPATH=$1?$2:${MAILPATH}
	fi
}

if [ -z "$BABEL_RM" -o "$BABEL_RM" = "yes" ]; then
	appendMailpath $MYMAILBOX '[babelfish] Youve got mail from `stat --format="%U" "$_"`
`basename "$_"`
`cat "$_"` `\rm -f "$_"`'
else
	appendMailpath $MYMAILBOX '[babelfish] Youve got mail from `stat --format="%U" "$_"`
`basename "$_"`
"`cat "$_"`"'
fi
appendMailpath $MAILBOX'present' '$(
	grep -E "`test -z "${FRIENDS}" && echo -n "" || echo "${FRIENDS}|"`harl" "$_" >/dev/null 2>/dev/null &&
		echo "[present] `cat "$_"` is here!" ||
		printf "\033[A"
)'

babelthrob() {
	local -a throb=("⣾" "⣽" "⣻" "⢿" "⡿" "⣟" "⣯" "⣷")
	local c=0
	local done=$(( $1 * 5 ))
	echo
	while [ $c -lt $done ]; do
		echo "\033[A${throb[$(( $c % ${#throb[@]}))]}"
		sleep 0.2
		c=$(( $c + 1 ))
	done
	echo -n "\033[A"
}


# the interfaces
babel() {
	if [ -z "$1" -o -z "$2" ]; then
		echo "[USAGE] babel \"<recipient>\" \"<subject>\""
		return 1
	elif [ ! -d "$MAILBOX/$1" ]; then
		echo "[ERROR] can't find mailbox \"$1\""
		return 1
	elif [ "$1" = "present" ]; then
		echo "[ERROR] don't write directly into \"present\""
		return 1
	fi
	cat > $MAILBOX/"$1"/"$2"
}

babelsearch() {
	find "$MAILBOX" -maxdepth 1 -type d -printf "%P\n" | grep -vE "present|.git|^$"
}

babelseat() {
	if [ -z "$1" ]; then
		echo "[USAGE] babelseat <seat>"
		return 1
	elif [ ! -e $MAILBOX'/present/'"$1" ]; then
		echo "[babel] Noone has been sitting at $1 recently"
	else
		echo "[babel] `cat $MAILBOX'/present/'"$1"` was there last"
	fi
}

announcePresence() {
	if [ "$1" ]; then
		echo "$1" >$MAILBOX'/present/'`hostname -s`
	else
		whoami >$MAILBOX'/present/'`hostname -s`
	fi
}

removePresence() {
	rm -f $MAILBOX'/present/'`hostname -s`
}

babelbacklog() {
	if [ -z "$BABEL_RM" -o "$BABEL_RM" = "yes" ]; then
		find $MYMAILBOX -type f -printf "Mail from %u\n%f\n" -execdir "cat" "{}" ";" -printf "\n" -delete | sh -c "${PAGER:-less}"
	else
		find $MYMAILBOX -type f -printf "Mail from %u\n%f\n" -execdir "cat" "{}" ";" -printf "\n" | sh -c "${PAGER:-less}"
	fi
}

babelgroupchat() {
	local subject="babel group message - $@ `whoami`"
	local body="`cat`"

	for user in $@ `whoami`; do
		echo -n "$body" | babel $user "$subject"
	done
	babelthrob 1 # ENTIRELY cosmetic. its fun tho
}

